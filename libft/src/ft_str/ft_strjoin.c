#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*strjoin;
	size_t	len;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2) + 1;
	strjoin = malloc(len);
	if (strjoin == NULL)
		return (NULL);
	ft_strlcpy(strjoin, s1, len);
	ft_strlcat(strjoin, s2, len);
	return (strjoin);
}
